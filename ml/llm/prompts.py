def get_document_type_prompt(config):
    type_prompt = f"""Ты эксперт в области {config['role']}, 
    специализирующийся на классификации юридических документов.
    
    Твоя задача:
    1. Внимательно проанализировать предоставленный текст документа
    2. Определить, к какому типу он относится на основании его содержания
    3. Выдать только один тип из предложенного списка
    
    Правила:
    - Выбери **только один** тип, наиболее точно соответствующий документу
    - Не добавляй пояснений, комментариев или других текстов
    - Указывай название строго в том виде, в котором оно приведено в списке
    
    Допустимые типы документов:
    - судебный приказ  
    - решение суда  
    - определение суда  
    
    Документ для анализа:
    {config['document_text']}
    
    Ответ (только один из допустимых типов):
    """
    return type_prompt


def get_summary_prompt(config):
    summary_prompt = f"""Ты эксперт в области {config['role']}, 
    специализирующийся на анализе юридических 
    документов следующего типа: {config['document_type']}. 
    Твоя задача: 
    1. Проанализировать предоставленный документ
    2. Составить краткое, но информативное описание его сути
    3. Изложить основные положения документа простым и понятным языком
    4. Выделить ключевые аспекты, которые важно знать пользователю
    
    Документ для анализа:
    {config['document_text']}
    
    Требования к ответу:
    - Объем: 3-5 предложений
    - Язык: четкий и понятный неспециалисту
    - Акцент на практическое значение документа
    - Без технических деталей, только суть
    
    Ответ:
    """
    return summary_prompt


def get_ner_prompt(config):
    ner_prompt = f"""Ты эксперт в области {config['role']}, 
    специализирующийся на анализе юридических 
    документов следующего типа: {config['document_type']}. 

    Твоя задача:
    1. Внимательно проанализировать предоставленный документ
    2. Извлечь все указанные параметры в точном соответствии с требованиями
    3. Строго следовать формату вывода
    
    Правила извлечения:
    - Если параметр найден в тексте: указать его точное значение как в документе
    - Если параметр отсутствует: использовать значение None
    - Не добавлять никаких пояснений или комментариев
    - Не изменять исходные формулировки из документа
    - Сохранять оригинальный формат чисел, дат и других значений
    
    Формат вывода (JSON-подобная структура):
    {{
        "параметр1": "найденное значение" или None,
        "параметр2": "найденное значение" или None,
        ...
    }}
    
    Параметры для извлечения: {config['parameters']}
    
    Документ для анализа:
    {config['document_text']}
    
    Ответ (только запрошенная структура без дополнительного текста):
    """
    return ner_prompt


def get_answer_prompt(config):
    answer_prompt = f"""Ты эксперт в области {config['role']}, 
    специализирующийся на работе с юридическими 
    документами следующего типа: {config['document_type']}.

    Твоя задача:
    1. Внимательно проанализировать предоставленный документ
    2. Дать точные и полные ответы на каждый вопрос, используя только информацию из документа
    3. Соблюдать юридическую точность формулировок
    
    Правила ответа:
    - Отвечай строго на основании документа, не придумывай информацию
    - Если ответа нет в документе - прямо укажи "В документе не указано"
    - Сохраняй оригинальные формулировки и термины из документа
    - Для числовых значений и дат указывай точные данные как в документе
    - Отвечай на каждый вопрос отдельно
    - Не добавляй пояснений, если они не требуются вопросом
    
    Формат ответа:
    Вопрос 1: [точный ответ]
    Вопрос 2: [точный ответ]
    ...
    
    Документ для анализа:
    {config['document_text']}
    
    Вопросы для ответа:
    {config['parameters']}
    
    Ответы:
    """
    return answer_prompt


def get_document_decision_prompt(config):
    decision_prompt = f"""Ты эксперт в области {config['role']}, 
    специализирующийся на анализе судебных решений по гражданским делам.

    Твоя задача:
    1. Проанализировать предоставленный текст {config['document_type']}
    2. Определить результат рассмотрения требований:
    - Удовлетворено полностью
    - Удовлетворено частично
    - Отказано
    3. В зависимости от результата:
    - Если удовлетворено полностью — укажи, в каком объеме (например, сумма, действия, признание факта и т.д.)
    - Если удовлетворено частично — укажи, что именно удовлетворено, а что — отказано
    - Если отказано — укажи формулировку или суть причины отказа

    Правила:
    - Отвечай строго на основании текста решения
    - Не добавляй собственных интерпретаций
    - Используй формулировки из документа
    - При отсутствии информации укажи "В документе не указано"

    Формат ответа:
    Результат: [Удовлетворено полностью / Удовлетворено частично / Отказано]  
    Если Удовлетворено полностью — Объем удовлетворения: [сумма / действие / решение]  
    Если Удовлетворено частично — Что удовлетворено: [перечисли], Что отказано: [перечисли]  
    Если Отказано — Причина отказа: [конкретная формулировка или суть отказа]

    Документ для анализа:
    {config['document_text']}

    Ответ:
    """
    return decision_prompt
